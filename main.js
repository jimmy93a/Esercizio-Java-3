let num1 = parseFloat(prompt("Inserisci il primo numero:"));
let num2 = parseFloat(prompt("Inserisci il secondo numero:"));

let somma = num1 + num2;
let sottrazzione = num1 - num2;
let moltiplicazione = num1 * num2;
let divisione = num1 / num2;
let potenza = num1 ** num2;

console.log(`Con i numeri da te scelti, i risultati delle varie operazioni sono: somma ${somma}, sottrazione ${sottrazzione}, moltiplicazione ${moltiplicazione}, divisione ${divisione}, potenza ${potenza}`);